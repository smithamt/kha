import { getUser } from "@/auth/user";
import { Toaster } from "@/components/ui/toaster";
import type { Metadata } from "next";
import Image from "next/image";
import { CustomQueryClientProvider } from "../contexts/tanstack";
import { HasUserProvider } from "../contexts/user";
import { VisitorProvider } from "../contexts/visitor";
import "../globals.css";
import "../index.css";
import Login from "../login/page";
import Navigation from "./_components/sidebar/navigation";
import Sidebar from "./_components/sidebar/sidebar";

export const metadata: Metadata = {
  title: "Dashboard",
  description: "Generated by create next app",
  icons: {
    icon: "/logo.png",
  },
};

export default async function RootLayout({
  children,
  create,
}: Readonly<{
  children: React.ReactNode;
  create?: React.ReactNode;
}>) {
  const user = await getUser();

  return (
    <html lang="en">
      <body className={""}>
        <VisitorProvider>
          {user ? (
            <CustomQueryClientProvider>
              <HasUserProvider user={JSON.stringify(user)}>
                <div className="w-full h-full flex">
                  {create}
                  <Sidebar />
                  <div className="flex-1">
                    <Navigation />
                    <div className="h-[calc(100%-56px)]">{children}</div>
                  </div>
                </div>
                <Toaster />
              </HasUserProvider>
            </CustomQueryClientProvider>
          ) : (
            <>
              <Image
                src={"/starlink.jpg"}
                fill
                alt="background"
                className="object-cover"
              />
              <Login searchParams={{}} />;
            </>
          )}
        </VisitorProvider>
      </body>
    </html>
  );
}
